// Mutlak değer almak için abs
Math.abs(-10);

// Floor => girilen ondalıklı sayıyı tabana yuvarlar
Math.floor(4.8);

// Ceil => girilen ondalıklı sayıyı tavana yuvarlar
Math.ceil(4.1);

// Üs almak için pow metodu kullanılabilir
Math.pow(2,5);

// SQRT square root yani kare kök
Math.sqrt(4);

// Trunc fonksiyonu ondalıklı kısmı direkt siler
Math.trunc(4.9);